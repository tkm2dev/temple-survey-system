-- ตารางจังหวัด (Provinces Master Data)
CREATE TABLE IF NOT EXISTS master_provinces (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL UNIQUE,
    region VARCHAR(50) NOT NULL COMMENT 'ภูมิภาค: เหนือ, ตะวันออกเฉียงเหนือ, กลาง, ใต้, ตะวันออก, ตะวันตก'
) COMMENT 'ตารางจังหวัด - ข้อมูลจังหวัดทั้ง 77 จังหวัด';

-- ตารางอำเภอ (Districts Master Data)
CREATE TABLE IF NOT EXISTS master_districts (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL,
    province_id INT NOT NULL COMMENT 'FK to master_provinces'
) COMMENT 'ตารางอำเภอ - ข้อมูลอำเภอทั่วประเทศ';

-- ตารางตำบล (Subdistricts Master Data)
CREATE TABLE IF NOT EXISTS master_subdistricts (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL,
    district_id INT NOT NULL COMMENT 'FK to master_districts',
    postal_code VARCHAR(5) COMMENT 'รหัสไปรษณีย์'
) COMMENT 'ตารางตำบล - ข้อมูลตำบลทั่วประเทศ';

